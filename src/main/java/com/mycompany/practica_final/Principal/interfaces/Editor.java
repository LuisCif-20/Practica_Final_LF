    private void analizadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_analizadorActionPerformed
        // TODO add your handling code here:
        if (this.jTextArea1.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "No hay texto para analizar");
        } else {
            AlmacenadorInfo.getDatosTabla().clear();
            String texto = this.jTextArea1.getText();
            stk = new Separador_Token(texto);
            while(stk.getPosicion() < texto.length()){
                stk.separarTexto();
            }
        }
    }//GEN-LAST:event_analizadorActionPerformed

