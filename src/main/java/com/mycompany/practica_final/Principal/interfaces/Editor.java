    //Metodo que ejecuta una accion cuando el boton abrir es presionado
    private void bAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAbrirActionPerformed
        //Creamos el objeto JFileChooser
        JFileChooser fc = new JFileChooser();
        int seleccion = fc.showOpenDialog(this);
        // Escogemos el fichero
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            this.jTextArea1.setText("");
            File archivo = fc.getSelectedFile();
            String ruta = archivo.getAbsolutePath();
            LectorArchivo.setRuta(archivo.getAbsolutePath());
            try {
                LectorArchivo.leerArchivo(ruta, jTextArea1);
                this.setTextoGuardado(this.jTextArea1.getText());
            } catch (IOException ex) {
                Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bAbrirActionPerformed

    //Metodo que ejecuta una accion cuando el boton Nuevo es presionado
    private void bNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bNuevoActionPerformed
        if (this.getTextoGuardado().equals(this.jTextArea1.getText())) {
            this.jTextArea1.setText("");
            LectorArchivo.setRuta("");
        } else {
            int opcion = JOptionPane.showConfirmDialog(null, "Â¿Deseas iniciar otro archivo sin guardar los cambios?", "Alerta", JOptionPane.YES_NO_OPTION);
            if (opcion == 0) {
                this.jTextArea1.setText("");
                LectorArchivo.setRuta("");
            }
        }
    }//GEN-LAST:event_bNuevoActionPerformed

    //Metodo que ejecuta una accion cuando el boton guardar es presionado
    private void bGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGuardarActionPerformed
        if (LectorArchivo.getRuta().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No existe una ruta para guardar los cambios");
        } else {
            LectorArchivo.guardarCambios(jTextArea1);
            this.setTextoGuardado(this.jTextArea1.getText());
        }

    }//GEN-LAST:event_bGuardarActionPerformed

    private void bGComoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGComoActionPerformed
        String nombre = JOptionPane.showInputDialog("Ingresa el nombre de tu archivo") + ".txt";
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No ingresaste un nombre");
        } else {
            JFileChooser fc = new JFileChooser();
            fc.setDialogTitle("Guardar");
            //Indicamos que solo la ruta de los directorios jale
            fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            int seleccion = fc.showOpenDialog(this);
            //Escogemos el fichero
            if (seleccion == JFileChooser.APPROVE_OPTION) {
                String rutaAbsoluta = fc.getSelectedFile() + File.separator + nombre;
                LectorArchivo.setRuta(rutaAbsoluta);
                LectorArchivo.guardarCambios(jTextArea1);
            }
        }
    }//GEN-LAST:event_bGComoActionPerformed

